/* ============================================================
   EduPulse — filegen.js
   Turns a section's own `preview` text into a REAL, valid file —
   .docx (Documents, with a one-click .pdf export) or .pptx (Presentation)
   — built client-side from the vendored libraries in assets/vendor/. No server, no network
   at demo time; no HTML mimic in a new tab.

   Single source of truth: `preview`. Editing a section's preview
   (editSecModal) changes exactly what the real file contains next
   time it's opened — there is no separate content copy to go stale.
============================================================ */

/* ---- text → structure (generic; works on any section's preview) ---- */
function parsePreviewToParagraphs(preview){
  const lines=(preview||'').split('\n').map(l=>l.trim()).filter(Boolean);
  if(!lines.length) return [{heading:null, body:'No content drafted yet — open Edit to add it.'}];
  return lines.map(line=>{
    const m=line.match(/^(?:\d+\.\s*)?(.{3,60}?)\s+—\s+(.+)$/);
    return m ? {heading:m[1], body:m[2]} : {heading:null, body:line};
  });
}
function parsePreviewToRubricRows(preview){
  const lines=(preview||'').split('\n').map(l=>l.trim()).filter(Boolean);
  const rows=[];
  lines.forEach(line=>{
    const m=line.match(/^(.+?)\s*[—-]\s*(\d+)\s*pts?\.?$/i);
    if(m) rows.push({criteria:m[1].trim(), points:Number(m[2])});
  });
  return rows;
}
function parsePreviewToSlideTitles(preview){
  const chunks=(preview||'').split('·').map(c=>c.trim()).filter(Boolean);
  const titles=chunks.map(c=>{
    const m=c.match(/^S\d+\s+(.*)$/);
    return (m?m[1]:c).trim();
  }).filter(t=>t && t!=='…');
  return titles.length?titles:['Overview'];
}

/* ---- structure → readable HTML, for the in-app viewer screen (same
   parsed structure the real file builders below consume, so what's on
   screen always matches what the downloaded file contains) ---- */
function escapeHtml(s){
  return String(s==null?'':s).replace(/[&<>"']/g,c=>({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]));
}
function paragraphsViewHtml(paras){
  return paras.map(p=>p.heading
    ?`<div class="kv" style="margin-top:14px"><b>${escapeHtml(p.heading)}</b>${escapeHtml(p.body)}</div>`
    :`<p style="font-size:13.5px;line-height:1.6;margin:8px 0;color:#2A3055">${escapeHtml(p.body)}</p>`
  ).join('');
}
function slidesViewHtml(titles){
  return `<div class="grid g2" style="margin-top:8px">
    ${titles.map((t,i)=>`<div class="card" style="text-align:center;padding:22px 14px"><div class="lock-note" style="margin-bottom:6px">Slide ${i+1}</div><b style="font-size:14px">${escapeHtml(t)}</b></div>`).join('')}
  </div>`;
}

/* ---- real file builders — one per format, all return Promise<Blob> ---- */
async function buildDocxBlob({title,courseCode,courseTitle,topicNo,topicTitle,docType,preview}){
  const {Document,Packer,Paragraph,TextRun,HeadingLevel}=docx;
  const paras=parsePreviewToParagraphs(preview);
  const children=[
    new Paragraph({children:[new TextRun({text:`${courseCode} — ${courseTitle} · Topic ${topicNo}: ${topicTitle}`,size:18,color:'697089'})],spacing:{after:60}}),
    new Paragraph({children:[new TextRun({text:(docType||'Document').toUpperCase(),bold:true,size:16,color:'3D5AF1'})],spacing:{after:220}}),
    new Paragraph({text:title,heading:HeadingLevel.HEADING_1,spacing:{after:200}}),
    ...paras.flatMap(p=>p.heading
      ?[new Paragraph({text:p.heading,heading:HeadingLevel.HEADING_2,spacing:{before:180,after:70}}),
        new Paragraph({children:[new TextRun(p.body)],spacing:{after:120}})]
      :[new Paragraph({children:[new TextRun(p.body)],spacing:{after:120}})]),
    new Paragraph({children:[new TextRun({text:'Generated by EduPulse (simulated AI) · instructor-reviewed before publishing to students.',size:16,italics:true,color:'697089'})],spacing:{before:280}})
  ];
  const doc=new Document({sections:[{children}]});
  return Packer.toBlob(doc);
}
function buildPdfBlob({title,courseCode,courseTitle,topicNo,topicTitle,docType,preview}){
  const {jsPDF}=window.jspdf;
  const paras=parsePreviewToParagraphs(preview);
  const rubric=parsePreviewToRubricRows(preview);
  const doc=new jsPDF({unit:'pt',format:'letter'});
  const x=48; const maxW=512; let y=56;
  const addWrapped=(text,size,gapAfter)=>{
    doc.setFontSize(size);
    doc.splitTextToSize(text,maxW).forEach(line=>{
      if(y>740){doc.addPage();y=56;}
      doc.text(line,x,y); y+=size*1.3;
    });
    y+=gapAfter;
  };
  doc.setTextColor(105,112,137); addWrapped(`${courseCode} — ${courseTitle} · Topic ${topicNo}: ${topicTitle}`,10,4);
  doc.setTextColor(61,90,241); addWrapped((docType||'Document').toUpperCase(),9,18);
  doc.setTextColor(27,37,89); addWrapped(title,17,16);
  doc.setTextColor(23,26,63);
  paras.forEach(p=>{
    if(p.heading) addWrapped(p.heading,13,4);
    addWrapped(p.body,11,10);
  });
  if(rubric.length){
    addWrapped('Scoring Rubric',13,4);
    rubric.forEach(r=>addWrapped(`${r.criteria} — ${r.points} pts`,11,4));
  }
  doc.setTextColor(105,112,137); addWrapped('Generated by EduPulse (simulated AI) · instructor-reviewed before publishing to students.',9,0);
  return Promise.resolve(doc.output('blob'));
}
async function buildPptxBlob({title,courseCode,courseTitle,topicNo,topicTitle,preview}){
  const p=new PptxGenJS();
  p.defineLayout({name:'EDU',width:10,height:5.63}); p.layout='EDU';
  const t1=p.addSlide();
  t1.addText(title,{x:0.5,y:1.5,w:9,h:1.2,fontSize:28,bold:true,color:'1B2559'});
  t1.addText(`${courseCode} — ${courseTitle}\nTopic ${topicNo}: ${topicTitle}`,{x:0.5,y:2.8,w:9,h:0.9,fontSize:14,color:'3D5AF1'});
  parsePreviewToSlideTitles(preview).forEach(st=>{
    const sl=p.addSlide();
    sl.addText(st,{x:0.5,y:2.4,w:9,h:1,fontSize:22,bold:true,color:'1B2559',align:'center'});
  });
  const last=p.addSlide();
  last.addText('Generated by EduPulse (simulated AI)\ninstructor-reviewed before publishing to students.',{x:0.5,y:2.3,w:9,h:1,fontSize:12,color:'697089',align:'center'});
  return p.write({outputType:'blob'});
}

/* ---- trigger a real browser download of a built Blob ---- */
function downloadBlob(blob,filename){
  const url=URL.createObjectURL(blob);
  const a=document.createElement('a');
  a.href=url; a.download=filename; document.body.appendChild(a); a.click();
  document.body.removeChild(a);
  setTimeout(()=>URL.revokeObjectURL(url),4000);
}
function slugify(s){return (s||'file').replace(/[^\w\- ]+/g,'').trim().replace(/\s+/g,'_').slice(0,60);}

/* ---- section → real file, one call site shared by instructor Course
   Content and student My Subjects (replaces the old HTML-mimic opener).
   Opens an in-app viewer screen (openFileViewer, below) so the content can
   be scanned right away; a Download button on that screen is what actually
   saves the real .docx/.pptx/.pdf — the same Blob already built for
   the preview, not rebuilt on click. ---- */
const DOC_BUILDERS={word:{build:buildDocxBlob,ext:'docx'}, pdf:{build:buildPdfBlob,ext:'pdf'}};
function sectionFileCtx(s,ctx){
  const docType=s.t==='file'?'Instructor Upload':(CONTENT_CATS[s.t]||'Document');
  return {title:s.label, courseCode:ctx.courseCode, courseTitle:ctx.courseTitle, topicNo:ctx.topicNo, topicTitle:ctx.topicTitle, docType, preview:s.preview};
}
function setDownloadHandler(blob,filename){
  window.__filegenDownload=()=>downloadBlob(blob,filename);
}
function revokeLastViewerUrl(){
  if(window.__filegenLastUrl){ URL.revokeObjectURL(window.__filegenLastUrl); window.__filegenLastUrl=null; }
}
function pdfViewerHtml(blob){
  revokeLastViewerUrl();
  const url=URL.createObjectURL(blob); window.__filegenLastUrl=url;
  return `<iframe src="${url}" style="width:100%;height:44vh;border:1px solid var(--line);border-radius:12px;margin-top:8px"></iframe>`;
}
/* An anchored popover (not the page-level sheet or a centered modal) — it
   stacks correctly above an already-open subject/topic sheet without
   clobbering it (sheet is a singleton overlay), and matches this app's
   "individual popover, not a blocking popup" pattern for scanning content. */
function openFileViewer(icon,title,sub,bodyHtml,ext,evt){
  openPop(`<h4>${icon} ${title}</h4>
  <div class="lock-note" style="margin-bottom:10px">${sub}</div>
  ${bodyHtml}
  <div class="pop-acts">
    <button class="btn btn-o btn-s" onclick="closePop()">Close</button>
    <button class="btn btn-p btn-s" onclick="__filegenDownload()">⬇ Download real file (.${ext})</button>
  </div>`, evt, 560);
}
async function openSection(s,ctx,evt){
  if(!s) return;
  if(s.t==='url'){ window.open(s.url,'_blank'); return; }
  if(s.t!=='doc'&&s.t!=='ppt'&&s.t!=='file'){ toast('No document to open for this type.'); return; }
  closePop();
  const fctx=sectionFileCtx(s,ctx||{});
  toast('Preparing preview…');
  try{
    if(s.t==='ppt'){
      const blob=await buildPptxBlob(fctx);
      const filename=slugify(s.label)+'.pptx';
      setDownloadHandler(blob,filename);
      openFileViewer('📽️', s.label, `${fctx.courseCode} · Topic ${fctx.topicNo}: ${fctx.topicTitle} · Presentation preview — real file: ${filename}`,
        `<div class="note-ai note" style="margin-top:0">Viewing the generated content directly — no download needed to scan it. The real, downloadable <b>.pptx</b> holds these exact slides.</div>
        ${slidesViewHtml(parsePreviewToSlideTitles(s.preview))}`, 'pptx', evt);
    } else {
      const fmt=DOC_BUILDERS[s.format]?s.format:'word';
      const blob=await DOC_BUILDERS[fmt].build(fctx);
      const filename=slugify(s.label)+'.'+DOC_BUILDERS[fmt].ext;
      setDownloadHandler(blob,filename);
      let bodyHtml;
      if(fmt==='pdf'){
        bodyHtml=`<div class="note-ai note" style="margin-top:0">Viewing the real generated <b>.pdf</b> directly — no download needed to scan it.</div>${pdfViewerHtml(blob)}`;
      } else {
        bodyHtml=`<div class="note-ai note" style="margin-top:0">Viewing the generated content directly — no download needed to scan it. The real, downloadable <b>.docx</b> holds this exact text.</div>
          ${paragraphsViewHtml(parsePreviewToParagraphs(s.preview))}`;
      }
      openFileViewer(TICON[s.t][0], s.label, `${fctx.courseCode} · Topic ${fctx.topicNo}: ${fctx.topicTitle} · ${fctx.docType} preview — real file: ${filename}`,
        bodyHtml, DOC_BUILDERS[fmt].ext, evt);
    }
  }catch(e){
    toast('Could not build the file — see console for details.');
    console.error(e);
  }
}
async function exportSectionPdf(s,ctx,evt){
  closePop();
  const fctx=sectionFileCtx(s,ctx||{});
  toast('Preparing PDF preview…');
  try{
    const blob=await buildPdfBlob(fctx);
    const filename=slugify(s.label)+'.pdf';
    setDownloadHandler(blob,filename);
    openFileViewer('📄', s.label+' (PDF export)', `${fctx.courseCode} · Topic ${fctx.topicNo}: ${fctx.topicTitle} · PDF preview — real file: ${filename}`,
      `<div class="note-ai note" style="margin-top:0">Viewing the real generated <b>.pdf</b> directly — no download needed to scan it.</div>${pdfViewerHtml(blob)}`, 'pdf', evt);
  }catch(e){
    toast('Could not build the PDF — see console for details.');
    console.error(e);
  }
}
